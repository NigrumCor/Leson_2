public class Lesson_2 {
    public static void main(String[] args) {
        for (int i = 0; i < 10 + 1; i++)
            System.out.println(i);


        int j = 0;
        while (j < 10 + 1) {

            System.out.println(j);
            j += 1;
        }
        int h = 0;
        do {
            h++;
            System.out.println(h);
        } while (h < 10);
    }


}
